package com.assignment1.array;

public class MatrixAddition {
	
	public static void main(String[] args) {
		// Matrix Addition
		
int [][] A = {{4,6,8}, {6,2,7}};
int [][] B = {{5,9,3}, {9,4,1}};

//materixaddition(A, B);

for (int i = 0; i < A.length; i++) {
	for (int j = 0; j < A[0].length; j++) {
	System.out.print(A[i][j] + B[i][j] + "  ");
	
}
System.out.println();
 
 
 }
}}
	

	
	
	
